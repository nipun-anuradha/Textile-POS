    public void updateTotal() {
        double subTotal = 0;
        double total = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String t = jTable1.getValueAt(i, 6).toString();
            subTotal = subTotal + Double.parseDouble(t);
        }
        jLabel6.setText(df.format(subTotal));

        String d = jTextField3.getText();
        if (!d.equals("0") && !d.isEmpty()) {

            int mark = jComboBox1.getSelectedIndex();
            if (mark == 0) {
                total = subTotal - (subTotal / 100) * Integer.parseInt(jTextField3.getText());
            } else {
                total = subTotal - Integer.parseInt(jTextField3.getText());
            }
            jLabel10.setText(df.format(total));
        } else {
            jLabel10.setText(df.format(subTotal));
        }

        balance();
    }

