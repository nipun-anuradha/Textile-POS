    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String sid = jLabel23.getText();
        String qty = jTextField7.getText();

        if (sid.equals("No select")) {
            JOptionPane.showMessageDialog(this, "Please enter product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("[1-9][0-9]*").matcher(qty).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid quentity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `stock` INNER JOIN `product` ON `Product`.id=`stock`.product_id INNER JOIN `category` ON `product`.category_id=`category`.id INNER JOIN `brand` ON `brand`.id=`product`.brand_id WHERE `stock`.id='" + sid + "' ");
                rs.next();
                String availableQty = rs.getString("stock.qty");
                String name = rs.getString("product.name");
                String category = rs.getString("category.name");
                String brand = rs.getString("brand.name");
                String price = rs.getString("stock.selling_price");

                if (Integer.parseInt(availableQty) < Integer.parseInt(qty)) {
                    JOptionPane.showMessageDialog(this, "Quantity out of stock", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                    boolean isFound = false;
                    int x = -1;

                    for (int i = 0; i < dtm.getRowCount(); i++) {
                        String s = jTable1.getValueAt(i, 0).toString();
                        if (s.equals(sid)) {
                            isFound = true;
                            x = i;
                            break;
                        }
                    }

                    //add
                    if (isFound) {
                        int option = JOptionPane.showConfirmDialog(this, "This product is already added.Do you want to update", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                        if (option == JOptionPane.YES_OPTION) {

                            int oldQty = Integer.parseInt(jTable1.getValueAt(x, 5).toString());
                            int finalQty = oldQty + Integer.parseInt(qty);

                            //check stock
                            if (Integer.parseInt(availableQty) < finalQty) {
                                JOptionPane.showMessageDialog(this, "Quantity out of stock", "Warning", JOptionPane.WARNING_MESSAGE);
                            } else {
                                jTable1.setValueAt(String.valueOf(finalQty), x, 5);

                                double updatedItemTotal = finalQty * Double.parseDouble(price);
                                jTable1.setValueAt(String.valueOf(updatedItemTotal), x, 6);

                                updateTotal();
                            }
                            //check stock
                        }
                        resetFields();

                    } else {
                        Vector v = new Vector();
                        v.add(sid);
                        v.add(name);
                        v.add(category);
                        v.add(brand);
                        v.add(price);
                        v.add(qty);

                        double itemTotal = Integer.parseInt(qty) * Double.parseDouble(price);
                        v.add(df.format(itemTotal));

                        dtm.addRow(v);

                        updateTotal();
                        resetFields();

//                        JOptionPane.showMessageDialog(this, "Product added to the INVOICE", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                    //add
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

