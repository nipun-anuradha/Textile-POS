    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 1) {
            int r = jTable1.getSelectedRow();
            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a user", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String type = jTable1.getValueAt(r, 5).toString();
                if (type.equals("Admin")) {
                    jButton3.setText("Make As Cachier");
                } else {
                    jButton3.setText("Make As Admin");
                }

                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Select user to change type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            int option = JOptionPane.showConfirmDialog(this, "Do you want to change user type?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

            if (option == JOptionPane.YES_OPTION) {

                String id = jTable1.getValueAt(selectedRow, 0).toString();
                String currentType = jTable1.getValueAt(selectedRow, 5).toString();

                int type;
                if (currentType.equals("Admin")) {
                    type = 2;
                } else {
                    type = 1;
                }

                MySQL.iud("UPDATE `user` SET `user_type_id`=" + type + " WHERE `id`=" + Integer.parseInt(id) + " ");

                loadUsers();

                JOptionPane.showMessageDialog(this, "User type updated", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

