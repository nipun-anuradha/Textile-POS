    public void loadUserType() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `user_type`");
            Vector v = new Vector();
            v.add("Select");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadUsers() {
        try {
            ResultSet rs = MySQL.search("SELECT `user`.`id`, `user`.`fname`, `user`.`lname`, `user`.`contact_no`, `user`.`password`, `user_status`.`name` AS `user_status`, `user_type`.`name` AS `user_type` FROM `user` INNER JOIN `user_type` ON `user`.user_type_id=`user_type`.id INNER JOIN `user_status` ON `user`.user_status_id=`user_status`.id ORDER BY `user`.id ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("fname"));
                v.add(rs.getString("lname"));
                v.add(rs.getString("contact_no"));
                v.add(rs.getString("password"));
                v.add(rs.getString("user_type"));
                v.add(rs.getString("user_status"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
